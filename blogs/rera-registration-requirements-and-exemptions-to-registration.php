        <p>There are exceptions to this rule. As Section 3(2) states that in case of Real estate project not exceeding 500 sqft or having less than 8 apartments, registration is not required.</p>

        <p class="">
            <span class="fw-bold theme_color_secondary">However in West bengal, Notification No 42-H4/2M-01/2023 </span> has been issued which provided that  exemption from registration is only acceptable when the threshold area of the land proposed to be developed <span class="fw-bold theme_color_secondary">does not exceed 200 sq. mtr (2152.78 sqft | 3 Kattha)  OR  Apartment ≤ 6</span>
        </p>

        <p class="">
            So , it could be interpreted that if the land to be developed is in excess of  200sqmtr (or roughly 3 katha) but only has 6 apartment then no registration required as the condition here is not dually satisfied.
        </p>

